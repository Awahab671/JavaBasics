package com.syntax.class03;

public class relationalOperators {
	public static void main(String[]args) {
		
		int num1 = 10;
		int num2 = 20;
		System.out.println(num1>num2);// greater than
		System.out.println( num1<num2);// smaller than
		System.out.println(num1!=num2);// not equal to 
		System.out.println(num1==num2);// equal to
		System.out.println(num1>=num2);//equal to greater than
		System.out.println(num1<=num2);// smaller or equal to

		
		
	}
}
